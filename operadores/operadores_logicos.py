# and
resultado = True & True # devuelve true
print(resultado)
resultado1 = False & True # devuelve falso
print(resultado1)
resultado2 = True & False # devuelve falso
print(resultado2)
resultado3 = False & False # devuelve falso
print(resultado3)
# or
resultado4 = True | True # devuelve true
print(resultado4)
resultado5 = False | True # devuelve true
print(resultado5)
resultado6 = True | False # devuelve true
print(resultado6)
resultado7 = False | False # devuelve false
print(resultado7)
# not
resultado8 = not True # devuelve falso
print(resultado8)
resultado9 = not False # devuelve verdadero
print(resultado9)